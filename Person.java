public class Person {
    String Name;
    public void setName(String name) {
        Name = name;
    }

    String Age;
    public void setAge(String age) {
        Age = age;
    }

    String Gender;

    public void setGender(String gender) {
        Gender = gender;
    }


    Person(){

    }
    

    public String getName() {
        return Name;
    }
    public String getAge() {
        return Age;
    }

    public String getGender() {
        return Gender;
    }
    
    
}
